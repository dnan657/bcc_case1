# push_generator.py
import locale

# –ù–∞—Å—Ç—Ä–æ–π–∫–∞ –ª–æ–∫–∞–ª–∏ –¥–ª—è –∫–æ—Ä—Ä–µ–∫—Ç–Ω–æ–≥–æ –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è –º–µ—Å—è—Ü–µ–≤ –Ω–∞ —Ä—É—Å—Å–∫–æ–º
try:
    locale.setlocale(locale.LC_TIME, 'ru_RU.UTF-8')
except locale.Error:
    locale.setlocale(locale.LC_TIME, 'Russian')

def format_kzt(amount):
    """–§–æ—Ä–º–∞—Ç–∏—Ä—É–µ—Ç —á–∏—Å–ª–æ –≤ –¥–µ–Ω–µ–∂–Ω—ã–π —Ñ–æ—Ä–º–∞—Ç KZT –ø–æ —Ä–µ–¥–ø–æ–ª–∏—Ç–∏–∫–µ."""
    if amount is None or amount == 0:
        return "0 ‚Ç∏"
    # –û–∫—Ä—É–≥–ª—è–µ–º –¥–æ —Å–æ—Ç–µ–Ω –¥–ª—è "–∫—Ä–∞—Å–∏–≤—ã—Ö" —á–∏—Å–µ–ª –≤ –ø—É—à–∞—Ö
    return f"{int(round(amount, -2)):,}".replace(',', ' ') + " ‚Ç∏"

def generate_push(client, product):
    """
    –ì–µ–Ω–µ—Ä–∏—Ä—É–µ—Ç –≥–∏–ø–µ—Ä-–ø–µ—Ä—Å–æ–Ω–∞–ª–∏–∑–∏—Ä–æ–≤–∞–Ω–Ω—ã–µ —Ç–µ–∫—Å—Ç—ã, –∞–¥–∞–ø—Ç–∏—Ä–æ–≤–∞–Ω–Ω—ã–µ –ø–æ–¥ –∫–ª–∏–µ–Ω—Ç–∞ –∏ –ø—Ä–æ–¥—É–∫—Ç.
    """
    name = client['name']
    status = client.get('status', '–°—Ç–∞–Ω–¥–∞—Ä—Ç–Ω—ã–π –∫–ª–∏–µ–Ω—Ç')

    if product == '–ö–∞—Ä—Ç–∞ –¥–ª—è –ø—É—Ç–µ—à–µ—Å—Ç–≤–∏–π':
        monthly_spend = client['travel_spend_3m'] / 3
        cashback = monthly_spend * 0.04
        return f"{name}, –≤ –ø—Ä–æ—à–ª–æ–º –º–µ—Å—è—Ü–µ –≤—ã –ø–æ—Ç—Ä–∞—Ç–∏–ª–∏ –Ω–∞ —Ç–∞–∫—Å–∏ –∏ –ø–æ–µ–∑–¥–∫–∏ –æ–∫–æ–ª–æ {format_kzt(monthly_spend)}. –° –∫–∞—Ä—Ç–æ–π –¥–ª—è –ø—É—Ç–µ—à–µ—Å—Ç–≤–∏–π –≤–µ—Ä–Ω—É–ª–∏ –±—ã ‚âà{format_kzt(cashback)}. –°—Ç–æ–∏—Ç –ø–æ–ø—Ä–æ–±–æ–≤–∞—Ç—å! üöï"

    if product == '–ü—Ä–µ–º–∏–∞–ª—å–Ω–∞—è –∫–∞—Ä—Ç–∞':
        return f"{name}, –≤–∞—à —Å—Ç–∞—Ç—É—Å –∏ –±–∞–ª–∞–Ω—Å –æ—Ç–∫—Ä—ã–≤–∞—é—Ç –ª—É—á—à–∏–µ —É—Å–ª–æ–≤–∏—è. –ü–æ–ª—É—á–∞–π—Ç–µ –¥–æ 4% –∫–µ—à–±—ç–∫–∞, –±–µ—Å–ø–ª–∞—Ç–Ω—ã–µ –ø–µ—Ä–µ–≤–æ–¥—ã –∏ —Å–Ω—è—Ç–∏–µ –Ω–∞–ª–∏—á–Ω—ã—Ö –ø–æ –≤—Å–µ–º—É –º–∏—Ä—É —Å –ø—Ä–µ–º–∏–∞–ª—å–Ω–æ–π –∫–∞—Ä—Ç–æ–π. –ü–æ–¥–∫–ª—é—á–∏—Ç–µ —Å–µ–π—á–∞—Å."

    if product == '–ö—Ä–µ–¥–∏—Ç–Ω–∞—è –∫–∞—Ä—Ç–∞':
        cat1 = client.get('top_1_category', '').lower()
        cat2 = client.get('top_2_category', '').lower()
        push_text = f"{name}, –∑–∞–º–µ—Ç–∏–ª–∏, —á—Ç–æ –≤—ã —á–∞—Å—Ç–æ –ø–æ–∫—É–ø–∞–µ—Ç–µ –≤ –∫–∞—Ç–µ–≥–æ—Ä–∏—è—Ö ¬´{cat1}¬ª –∏ ¬´{cat2}¬ª. " if cat1 and cat2 else f"{name}, –≤–∞—à–∏ –æ–Ω–ª–∞–π–Ω-–ø–æ–∫—É–ø–∫–∏ –º–æ–≥—É—Ç –±—ã—Ç—å –µ—â–µ –≤—ã–≥–æ–¥–Ω–µ–µ. "
        push_text += "–° –∫—Ä–µ–¥–∏—Ç–Ω–æ–π –∫–∞—Ä—Ç–æ–π –Ω–∞ –Ω–∏—Ö –≤–µ—Ä–Ω–µ—Ç—Å—è –¥–æ 10% –∫–µ—à–±—ç–∫–∞. –û—Ñ–æ—Ä–º–∏—Ç—å –∫–∞—Ä—Ç—É."
        return push_text

    if product == '–î–µ–ø–æ–∑–∏—Ç –ú—É–ª—å—Ç–∏–≤–∞–ª—é—Ç–Ω—ã–π':
        return f"{name}, —Ö—Ä–∞–Ω–∏—Ç–µ —Å–±–µ—Ä–µ–∂–µ–Ω–∏—è –≤ —Ä–∞–∑–Ω—ã—Ö –≤–∞–ª—é—Ç–∞—Ö –∏ –ø–æ–ª—É—á–∞–π—Ç–µ –¥–æ—Ö–æ–¥. –û—Ç–∫—Ä–æ–π—Ç–µ –º—É–ª—å—Ç–∏–≤–∞–ª—é—Ç–Ω—ã–π –≤–∫–ª–∞–¥ —Å–æ —Å—Ç–∞–≤–∫–æ–π 14,5% –∏ —Å–≤–æ–±–æ–¥–Ω—ã–º –¥–æ—Å—Ç—É–ø–æ–º –∫ –¥–µ–Ω—å–≥–∞–º."

    if product == '–î–µ–ø–æ–∑–∏—Ç –°–±–µ—Ä–µ–≥–∞—Ç–µ–ª—å–Ω—ã–π':
        return f"{name}, —É –≤–∞—Å –µ—Å—Ç—å —Å–≤–æ–±–æ–¥–Ω—ã–µ —Å—Ä–µ–¥—Å—Ç–≤–∞. –ó–∞—Å—Ç–∞–≤—å—Ç–µ –∏—Ö —Ä–∞–±–æ—Ç–∞—Ç—å, –æ—Ç–∫—Ä—ã–≤ —Å–±–µ—Ä–µ–≥–∞—Ç–µ–ª—å–Ω—ã–π –≤–∫–ª–∞–¥ —Å –º–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–π —Å—Ç–∞–≤–∫–æ–π 16,5% –≥–æ–¥–æ–≤—ã—Ö. –û—Ç–∫—Ä—ã—Ç—å –≤–∫–ª–∞–¥."

    if product == '–ò–Ω–≤–µ—Å—Ç–∏—Ü–∏–∏':
        if status == '–°—Ç—É–¥–µ–Ω—Ç':
            return f"{name}, –Ω–∞—á–Ω–∏ –∏–Ω–≤–µ—Å—Ç–∏—Ä–æ–≤–∞—Ç—å –±–µ–∑ —Å–ª–æ–∂–Ω—ã—Ö —à–∞–≥–æ–≤! –ú–æ–∂–Ω–æ —Å—Ç–∞—Ä—Ç–æ–≤–∞—Ç—å –≤—Å–µ–≥–æ —Å 6 ‚Ç∏, –∞ –ø–µ—Ä–≤—ã–π –≥–æ–¥ ‚Äî –±–µ–∑ –∫–æ–º–∏—Å—Å–∏–π. –û—Ç–ª–∏—á–Ω—ã–π —Å—Ç–∞—Ä—Ç –¥–ª—è —Ç–≤–æ–µ–≥–æ –∫–∞–ø–∏—Ç–∞–ª–∞. –ü–æ–ø—Ä–æ–±–æ–≤–∞—Ç—å üìà"
        return f"{name}, –ø–æ–ø—Ä–æ–±—É–π—Ç–µ –∏–Ω–≤–µ—Å—Ç–∏—Ü–∏–∏ —Å –Ω–∏–∑–∫–∏–º –ø–æ—Ä–æ–≥–æ–º –≤—Ö–æ–¥–∞ –∏ –±–µ–∑ –∫–æ–º–∏—Å—Å–∏–π –Ω–∞ —Å—Ç–∞—Ä—Ç–µ. –≠—Ç–æ –ø—Ä–æ—Å—Ç–æ–π —Å–ø–æ—Å–æ–± –∑–∞—Å—Ç–∞–≤–∏—Ç—å –≤–∞—à–∏ –¥–µ–Ω—å–≥–∏ —Ä–∞–±–æ—Ç–∞—Ç—å. –û—Ç–∫—Ä—ã—Ç—å —Å—á—ë—Ç."

    # –û–±—â–∞—è "–∑–∞–≥–ª—É—à–∫–∞", –µ—Å–ª–∏ –≤–¥—Ä—É–≥ —á—Ç–æ-—Ç–æ –ø–æ—à–ª–æ –Ω–µ —Ç–∞–∫
    return f"{name}, —É –Ω–∞—Å –µ—Å—Ç—å —Å–ø–µ—Ü–∏–∞–ª—å–Ω–æ–µ –ø—Ä–µ–¥–ª–æ–∂–µ–Ω–∏–µ –¥–ª—è –≤–∞—Å. –£–∑–Ω–∞–π—Ç–µ –ø–æ–¥—Ä–æ–±–Ω–æ—Å—Ç–∏ –≤ –ø—Ä–∏–ª–æ–∂–µ–Ω–∏–∏."